// Get all goals for a specific life stage
export function getGoalsForStage(stageId) {
    return GOAL_TEMPLATES[stageId] || [];
}

// Get a specific goal template by ID across all stages
export function getGoalTemplateById(goalId) {
    for (const stage of Object.values(GOAL_TEMPLATES)) {
        const found = stage.find(g => g.id === goalId);
        if (found) return found;
    }
    return null;
}
